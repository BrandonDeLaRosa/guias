// !UNIDADES DE MEDICA EN CSS


//? px (píxeles): Unidades absolutas que representan un píxel en la pantalla. Es una medida fija que no cambia con el tamaño de la pantalla o el zoom.

//? em: Unidad relativa al tamaño de la fuente del elemento padre. Si el padre tiene una fuente de 16px, 1em será 16px.

//? rem (root em): Unidad relativa al tamaño de la fuente del elemento raíz (normalmente el <html>). Si el tamaño de la fuente en <html> es 16px, 1rem será 16px para todo el documento.

//? vw (viewport width): Representa un porcentaje del ancho de la ventana gráfica (la pantalla visible). 1vw es el 1% del ancho total.

//? vh (viewport height): Similar a vw, pero basado en la altura de la ventana gráfica. 1vh es el 1% de la altura total.

//? % (porcentaje): Unidad relativa al tamaño del contenedor padre. Se utiliza mucho en el diseño responsivo para ajustar anchos, alturas, márgenes, etc.

//? ch: Unidad que representa el ancho de un carácter 0 en la fuente actual del elemento. Es útil para ajustar el tamaño de textos o contenedores.

//? vmin y vmax: Unidades que representan el porcentaje del menor (vmin) o mayor (vmax) valor entre vw y vh.